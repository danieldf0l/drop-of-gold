    private void btnSalvarProduto1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarProduto1ActionPerformed
        String id_produto = txtIdProduto.getText();
        String quantidade = txtQuantidade.getText();
        String data_validade = txtDataValidade.getText();
        String numero_lote = txtNumeroLote.getText();
        
        int int_id_produto = Integer.parseInt(id_produto);
        int int_quantidade = Integer.parseInt(quantidade);
        
        Estoque estoque = new Estoque();
        estoque.setId_produto(int_id_produto);
        estoque.setQuantidade(int_quantidade);
        estoque.setData_validade(data_validade);
        estoque.setNumero_lote(numero_lote);

        Dao_Tabela_Estoque tabela_estoque = new Dao_Tabela_Estoque();
        tabela_estoque.Inserir_Dados(estoque);
    }//GEN-LAST:event_btnSalvarProduto1ActionPerformed

